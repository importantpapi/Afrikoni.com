// Get category by ID
export function getCategoryById(id) {
  return B2B_CATEGORIES.find(cat => cat.id === id);
}

// Get categories by group
export function getCategoriesByGroup(groupName) {
  const categoryIds = CATEGORY_GROUPS[groupName] || [];
  return B2B_CATEGORIES.filter(cat => categoryIds.includes(cat.id));
}

// Search categories
export function searchCategories(query) {
  const lowerQuery = query.toLowerCase();
  return B2B_CATEGORIES.filter(cat =>
    cat.name.toLowerCase().includes(lowerQuery) ||
    cat.description.toLowerCase().includes(lowerQuery) ||
    cat.subcategories.some(sub => sub.toLowerCase().includes(lowerQuery))
  );
}

